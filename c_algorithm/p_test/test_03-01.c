/*
整数の乱数を生成して5の倍数が生成された時はfivetimes.txtに書き込むプログラムを作成せよ。10個の5の倍数が書き込まれるまで続けること。
*/

#include <stdio.h>
#include <stdlib.h>

int main (void){

           FILE* fp;
           /*ファイルポインタの指定*/

           int i = 0;
           int r = 0;
           
           /*ファイルを書き込みモードでopen*/
           fp = fopen("fivetimes.txt", "w");
           if (fp == NULL) {
                      printf("ファイルfivetimes.txtを開けることができなかった。 \n");
                     
                      return -1;
           }
 
           while (i < 10){
               /*rという変数に疑似乱数を代入する*/
               r = rand();
               printf("%d\n", r);
               /*5の倍数であれば書き込んでiをカウントする*/
               if (r % 5 == 0) {
                   fprintf(fp, "%d\n", r);
                   i++;
               }
           }
           
           /*ファイルを閉じる*/
           fclose(fp);
 
           return 0;

}

